class User {
  final int id;
  final String name;
  final String gender;
  final int old;
  final String jobs;
  final String about;
  final String imageUrl;
  final bool online;
  final bool verified;
  final String location;

  User({
    this.id,
    this.name,
    this.gender,
    this.old,
    this.jobs,
    this.about,
    this.imageUrl,
    this.online,
    this.verified,
    this.location,
  });
}
